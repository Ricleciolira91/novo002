// ----------------- LÓGICA tipos de Variáveis

// var , let e const
// VAR - Quando rodamos uma variável que está no código mais de duas vezes, a declaração impressa
// será sempre da última vez que a variável foi chamada.

// LET - Não permite a criação de duas variáveis com o mesmo nome.

// CONST - Declarar uma constante, valor não seja alterado durante a execução do código.
// por exemplo - valor de PI = 3,141593
// valor não será alterado na aplicação.
// Também não permite ser declarado duas vezes na aplicação.












